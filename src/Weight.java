public class Weight {

    private int weight;
    private String date;

    public Weight(int weight, String date){
        this.weight = weight;
        this.date = date;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }


}
